package com.ming.springboot.pojo;

import org.springframework.data.annotation.Id;

public class User {

	//id属性是给mongodb用的，用@Id注解修饰
	@Id
	private String id;
	private Long uid;
	private String name;
	private Integer age;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getUid() {
		return uid;
	}

	public void setUid(Long uid) {
		this.uid = uid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
}
